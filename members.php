            <p>We all know that change is perhaps the only constant in life - even with the weather! Someone once told me after I moved to FL in 1998 that if I didn't like the weather to wait 10 minutes and it would change. While not as true in Key West, they were right about Central Florida anyway. That being said, we are undergoing many changes in our leadership areas at the moment but I wanted you to know that God is working in many amazing ways to fill in the gaps and I have complete confidence He will continue to do so as time goes on. I am reminded of the transition that occurred from Moses to Joshua prior to the Israelites entering the Promised Land. It was a time of challenge and uncertainty but also a time to see God's hand at work in a wonderful way. Here is some information for your prayerful consideration and ways in which you can help this transition time go smoothly.</p>
            <p>            1)  A month ago we said goodbye to the Rodriguez family as they moved to Boca Raton to help their sons transition to college life. They wore many hats here. Oscar was Deacon Chairman and helped with the overall administration of committees and church projects. Christine was our church secretary and kept things on that level running smoothly. She also was a part of our praise team. Brandon and Austin were very talented instrumentalists who are continuing their education in that area. You don't replace such gifted and sacrificial people easily. We wish them well in their new setting and in this new adventure for their lives. God never guides where He doesn't provide either for them or us. We wish them well.   
</p>
            <p>            2)  Vaughn Sommers has taken on the role of our new Deacon Chairman. He has served as a deacon for many years and has a servant's heart. He is a gifted teacher and leader who needs our prayers as he takes on this new responsibility. Please be in prayer for he and wife Gwen and give them your support.  Also be in prayer for your deacon ministry team as we prayerfully seek candidates to fill the deacon role as we are currently a couple short of the ideal number for our church family.
</p>
            <p>            3)  Daliana Goins continues to bless us with her musical gift. She has taken on an unofficial worship leader role for the past several months and will continue doing so. However we are a few members short of an ideal praise team number particularly in the area of instruments. If you would love to use your musical playing abilities in a God-honoring way she would love to hear from you. While using "canned" music is acceptable we would love to get back to a live praise band ASAP. Help if you can.
</p>
            <p>          4)  Linda Walker has graciously accepted the role of church secretary on a trial basis. She brings a great deal of office experience to the table having formerly worked for the School District but as with any new position there is always a learning curve to experience. For the time being her office hours will be Monday, Wednesday and Friday from 9am-Noon.  Please be in prayer for her, be patient with and support her as she takes on this new, challenging and important role.
</p>
            <p>               Others have stepped up to take helpful supporting roles for which we are grateful. Jeffrey Matthews Junior will be handling our church website. Others have offered to spend time in the office when necessary. It is wonderful to see the church responding to the needs at hand. September will be a busy month of ramping up after a slower summer. God is bringing new families into our services weekly whom we hope will transition into an active part of our fellowship. We are already beginning plans for the new church year beginning in January. We are starting two new studies for parenting and debt management help. Shortly we will begin a series of messages using the movie "God's Not Dead" as a catalyst. We will also screen a showing of the movie during an upcoming Sunday night service. Thank you so much for your understanding and support of Fifth Street and its ministries. God has great things in store for us! I look forward to seeing you Sunday with a bible in my hand and a message on my heart.
</p>
