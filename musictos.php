                <div class="tab-pane fade" id="guidelines">
                    <br />
                        <p><strong>Worship Team Guidelines</strong></p>
                    <p>The 5th Street Baptist Worship Team is entirely a volunteer ministry.
This list explains the process of joining the ministry and what is expected of the people
once they are a part of the church worship ministry team.</p>
                <ul>
                    <li><strong>Application Submitted Prior to Audition</strong></li>
                    <li><strong>Preliminary Interview completed on night of Audition</strong></li>
                    <li><strong>Audition (A spot may or may not be available for you currently on the team)</strong></li>
                    <li><strong>Probationary period </strong>- If you are called back there will ordinarily (exceptions at the discretion of the Team Leader) be a two month probationary period during which the individual will attend practice sessions but will not play/sing with the team during services. The purpose of this directive is to ascertain who is genuinely interested in helping lead worship for the glory of God, and who wishes to be in the team for other reasons. In addition, this period allows time for the individual and the rest of the team to gel musically.</li>
                    <li><strong>Weekly practice sessions </strong>-  All team members are required to attend the weekly
practice sessions. Whenever team members are scheduled to minister at a service, they are required to be in attendance. There may be exceptions to this requirement on occasion when necessary but not habitually.</li>
                    <li><strong>Other functions </strong>- The team will be expected to lead worship for special events at
5th Street Baptist, as well as any opportunities to minister around the community at outreach events (March for Jesus, Bayview Park festivals, etc.)</li>
                    <li><strong>Commitment to being spiritually prepared to minister</strong> - Our spiritual preparation
has a direct effect on our effectiveness as a music ministry team. We need to come ready not only to sing and play music but to worship the King and minister to His people. The essential element in this ministry (or any other for that matter) is our relationship with the Lord.  Without a consistent and growing relationship with the Lord, the worship team will not be effective <u><em><b>regardless of musical proficiency!</b></em></u></li>
                    <li><strong>Commitment to musical expertise</strong> - We need to work at our instrumental and/or
vocal ability to become the very best we can be. This means a great deal of individual
practice (maybe even lessons) as well as playing with the team. Keep in mind that the
team will only be as musically proficient as the individuals that make it up.</li>
                    <li><strong>Commitment to growing spiritually</strong> - This too is as important on an individual basis
as it is with the team. As a leadership ministry, we need to be committed to maturing in
Jesus. Stagnant spiritual lives means a stagnant worship team. Encourage and challenge one another.</li>
                    <li><strong>Commitment to “flowing” with the team</strong> - More than individual expertise, our
musical goal needs to be the ability to “flow” with the team. We need to look at our team
as not just a collection of separate musicians and singers but as a single unit with a single purpose. Our heart attitude should not be to shine our own technical skill but to use that skill to enhance the team as a whole. Worship ministry is a team effort and requires humility in relationship and the ability to listen to and respond to one another in a Christ-like manner.</li>
                    <li><strong>Dress standard</strong> - It is expected that members of the worship team will present
themselves at services in a manner both glorifying to God and acceptable to the human
eye considering that they are in an ‘up-front’ ministry. We need to stay away from
distracting dress and anything that draws attention away from our focus on the Father.
 </li>
                    <li><strong>Punctuality</strong> – Bottom line, keeping the team waiting is rude. Accurate timekeeping
will assist the goals of the worship team. Team members who continually disregard
starting times for rehearsals or services can expect a grace filled conversation with the
Team leader. If it is not possible to make a rehearsal or service on time, please notify the Team Leader in advance.</li>
                    </ul>    
