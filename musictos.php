                    <br />
                    <p><strong>Worship Team Criteria</strong></p>
                    <p>The following are a list of criteria indicating the preferred qualifications for a member of
the worship team in this church.</p>
                    <ul>
                        <li><strong>Committed member of this church</strong> - Music ministry is not the right of the talented but the privilege of the committed and faithful. We do not use “hired guns”. We are open to receiving special music on occasion from those outside the church but the regular, ongoing, upfront nature of this ministry requires a strong commitment to the people and eadership of this church.</li>
                        <li><strong>Worshipper </strong> - This team is as much about the heart as it is the art. To qualify for the worship team, one’s heart must be given to worship. Every member, musicians and singers alike, <em>must</em> have a love not just for performance but for praise and adoration of the Lord.</li>
                        <li><strong>Spiritual maturity </strong> - Spiritual and emotional maturity are necessary for worship team to function effectively. We’re all at different stages of the journey but members of 5th Street Baptist’s worship team are expected to be growing consistently in their walk with Christ and their capacity to function biblically as a team.</li>
                        <li><strong>Musical expertise </strong> - The musicians and vocalists need to be skilled enough to follow a service which is led by the Spirit of God. We don’t want to limit what God will do simply because we can’t handle it musically. - Note that the standard of musical proficiency required may vary as the church grows.  An audition will provide the music ministry leadership with the opportunity to evaluate your level of musical expertise, and how it may be used as part of the music ministry here at Fifth Street Baptist Church.</li>
                        <li><strong>Time commitment </strong> - Practice sessions, preparation before each service and ministry are mandatory. In addition, there may be seminars, special retreats, and seasonal events- The worship ministry requires a fairly large time commitment.</li>
                        <li><strong>Ability to flow with the team </strong> - Some people are very gifted musicians and/or vocalists, but for one reason or another (vocal range, musical style, relational capacity, etc.) they have trouble flowing with the team. These individuals are highly encouraged to share their musical gifts in other ways (small group worship, musical specials, etc) with the body. Musical proficiency does not automatically make someone a top candidate for the worship team.  The Senior Pastor, in cooperation with the Worship Team Leader reserve the right to the final decision concerning entry into the worship ministry at this church.</li>
                    </ul>
